  /**
   * 내장된 데이터를 기반으로 솔버를 초기화합니다.
   */
  static async load() {
    const protos = [];
    for (const [char, [width, height, data]] of Object.entries(CaptchaSolver.RAW_DATA)) {
      protos.push({
        char,
        width,
        height,
        data: new Uint8Array(data)
      });
    }
    return new CaptchaSolver(protos);
  }

  /**
   * 이미지 버퍼를 입력받아 6자리 보안문자 판독 결과를 반환합니다.
   */
  async solve(imgBuffer) {
    const detailed = await this.solveDetailed(imgBuffer);
    return detailed.map(item => item.char).join('');
  }

  async solveDetailed(imgBuffer) {
    // 1. 이미지 로드 및 배경 합성 (화이트 배경 강제)
    const { data, info } = await sharp(imgBuffer)
      .flatten({ background: { r: 255, g: 255, b: 255 } })
      .grayscale()
      .raw()
      .toBuffer({ resolveWithObject: true });

    const width = info.width;
    const height = info.height;
    const binaryArr = new Uint8Array(data.length);
    for (let i = 0; i < data.length; i++) {
      binaryArr[i] = data[i] < 150 ? 1 : 0;
    }

    // 2. 상단 노이즈 행 감지
    const noiseRows = new Set();
    for (let y = 0; y < Math.min(height, 30); y++) {
      let blackPixels = 0;
      for (let x = 0; x < width; x++) {
        if (binaryArr[y * width + x] === 1) blackPixels++;
      }
      if (blackPixels / width > 0.6) {
        noiseRows.add(y);
      }
    }

    // 3. 수학적 커닝 가중치 상수
    const getAdvance = (c) => {
      if (['1', '5'].includes(c)) return 18;
      if (['6', '7', '8'].includes(c)) return 19;
      return 20;
    };

    const getBearing = (c) => {
      if (c === '1') return 2;
      if (['5', '6', '7', '8'].includes(c)) return 1;
      return 0;
    };

    const usedDigits = new Set();
    const result = [];
    let lastX = 0;
    let lastChar = ' ';

    for (let slot = 0; slot < 6; slot++) {
      let slotBestChar = '?';
      let slotBestScore = -Infinity;
      let slotBestX = 0;

      for (const proto of this.protos) {
        if (usedDigits.has(proto.char)) continue;

        // 유효 픽셀 범위 계산
        let reachable = 0;
        for (let py = 0; py < proto.height; py++) {
          if (noiseRows.has(py)) continue;
          for (let px = 0; px < proto.width; px++) {
            if (proto.data[py * proto.width + px] === 1) reachable++;
          }
        }
        if (reachable === 0) continue;

        // 탐색 오프셋 설정 (커닝 적용)
        let searchOffsets = [];
        if (slot === 0) {
          for (let x = 9; x <= 11; x++) searchOffsets.push(x);
        } else {
          const base = lastX + getAdvance(lastChar) + getBearing(proto.char);
          for (let x = base - 2; x <= base + 2; x++) searchOffsets.push(x);
        }

        for (const offset of searchOffsets) {
          if (offset < 0 || offset + proto.width > width) continue;

          let hit = 0, miss = 0, noise = 0;
          for (let py = 0; py < proto.height; py++) {
            if (noiseRows.has(py)) continue;
            for (let px = 0; px < proto.width; px++) {
              const ax = offset + px;
              const pBlack = proto.data[py * proto.width + px] === 1;
              const aBlack = binaryArr[py * width + ax] === 1;

              if (pBlack && aBlack) hit++;
              else if (pBlack && !aBlack) miss++;
              else if (!pBlack && aBlack) noise++;
            }
          }

          // 가중치 스코어링 정책
          const score = (hit - miss * 2.0 - noise * 1.5) / reachable;
          if (score > slotBestScore) {
            slotBestScore = score;
            slotBestChar = proto.char;
            slotBestX = offset;
          }
        }
      }

      if (slotBestChar !== '?') {
        result.push({ char: slotBestChar, x: slotBestX });
        usedDigits.add(slotBestChar);
        lastX = slotBestX;
        lastChar = slotBestChar;
      } else {
        result.push({ char: '?', x: 0 });
      }
    }

    return result;
  }
